export class Country {
  private ID: number;
  private RegionID: number;
  private Name: string;
  private HappinessRank: number;
  private GDP: number;
  private Freedom: number;
  private GovTrust: number;
  private DystopiaResidual: number;

  constructor(
    ID: number,
    RegionID: number,
    Name: string,
    HappinessRank: number,
    GDP: number,
    Freedom: number,
    GovTrust: number,
    DystopiaResidual: number
  ) {
    this.ID = number;
    this.RegionID = number;
    this.Name = string;
    this.HappinessRank = number;
    this.GDP = number;
    this.Freedom = number;
    this.GovTrust = number;
    this.DystopiaResidual = number;
  }
}
